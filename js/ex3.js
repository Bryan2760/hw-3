const num1 = Number(prompt("Enter first number"));
const num2 = Number(prompt("Enter second number"));


console.log ("The following mathematical operations for the number entered:");

console.log (`${num1} + ${num2} =`, num1 + num2);
console.log (`${num1} - ${num2} =`, num1 - num2);
console.log (`${num1} * ${num2} =`, num1 * num2);
console.log (`${num1} / ${num2} =`, num1 / num2);
console.log (`${num1} % ${num2} =`, num1 % num2);
